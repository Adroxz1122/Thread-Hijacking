BOOL CreateProc(LPCSTR ProcName, DWORD *ProcPID, HANDLE *HProcess, HANDLE *HThread){
    CHAR FinalPath[MAX_PATH * 2];
    CHAR WnDr[MAX_PATH];

    STARTUPINFO SI = {0};
    PROCESS_INFORMATION PI = {0};

    RtlSecureZeroMemory(&SI, sizeof(STARTUPINFO));
    RtlSecureZeroMemory(&PI, sizeof(PROCESS_INFORMATION));

    SI.cb = sizeof(STARTUPINFO);

    if(!GetEnvironmentVariableA("WINDIR", WnDr, MAX_PATH)){
        printf("GetEnvironmentVariable failed");
        return FALSE;
    }

    sprintf(FinalPath, "%s\\System32\\%s", WnDr, ProcName);

    if(!CreateProcessA(
        NULL,
        FinalPath,
        NULL,
        NULL,
        FALSE,
        CREATE_SUSPENDED,
        NULL,
        NULL,
        &SI,
        &PI
    )){
        printf("CreateProcessA failed");
        return FALSE;
    }

    *ProcPID = PI.dwProcessId;
    *HProcess = PI.hProcess;
    *HThread = PI.hThread;

    if(*ProcPID != 0 && *HProcess != NULL && *HThread != NULL){
        return TRUE;
    }
    return FALSE;
}

